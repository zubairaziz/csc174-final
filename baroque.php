						<li>
							<strong>Allemande</strong> – Often the first dance of an instrumental suite, the allemande was a very popular dance that had
							its origins in the German Renaissance era. The allemande was played at a moderate tempo and could start on any beat
							of the bar.</li>
						<li>
							<strong>Courante</strong> – The second dance is the courante, a lively, French dance in triple meter. The Italian version
							is called the corrente.
						</li>
						<li>
							<strong>Sarabande</strong> – The sarabande, a Spanish dance, is the third of the four basic dances, and is one of the slowest
							of the baroque dances. It is also in triple meter and can start on any beat of the bar, although there is an emphasis
							on the second beat, creating the characteristic 'halting', or iambic rhythm of the sarabande.</li>
						<li>
							<strong>Gigue</strong> – The gigue is an upbeat and lively baroque dance in compound meter, typically the concluding movement
							of an instrumental suite, and the fourth of its basic dance types. The gigue can start on any beat of the bar and
							is easily recognized by its rhythmic feel. The gigue originated in the British Isles. Its counterpart in folk music
							is the jig.</li>
